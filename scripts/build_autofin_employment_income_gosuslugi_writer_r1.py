def esc(value: object) -> str:
    return str(value).replace("\n", "<br>").replace("|", "\\|")


def md_table(headers: list[str], rows: list[list[object]]) -> str:
    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    lines.extend("| " + " | ".join(esc(cell) for cell in row) + " |" for row in rows)
    return "\n".join(lines)


def bullets(items: list[str]) -> str:
    return "\n".join(f"- {item}" for item in items)


def write_markdown(target: Path, stem: str, title: str, sections: list[tuple[int, str, str]]) -> None:
    artifact_dir = SCRATCH / stem
    artifact_dir.mkdir(parents=True, exist_ok=True)
    manifest_sections = []
    for index, (level, heading, content) in enumerate(sections, start=1):
        content_file = artifact_dir / f"{index:02d}.md"
        content_file.write_text(content.strip() + "\n", encoding="utf-8", newline="\n")
        manifest_sections.append({"level": level, "heading": heading, "content_file": content_file.name})
    manifest = {
        "target_path": str(target.resolve()),
        "sections": manifest_sections,
    }
    if not sections or sections[0][1] != title:
        preamble = artifact_dir / "00-preamble.md"
        preamble.write_text(f"# {title}\n", encoding="utf-8", newline="\n")
        manifest["preamble_file"] = preamble.name
    manifest_path = artifact_dir / "manifest.json"
    manifest_path.write_text(json.dumps(manifest, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    subprocess.run(
        [sys.executable, str(ROOT / "scripts" / "write_artifact_sections.py"), "--manifest", str(manifest_path), "--dry-run"],
        cwd=ROOT,
        check=True,
    )
    subprocess.run(
        [sys.executable, str(ROOT / "scripts" / "write_artifact_sections.py"), "--manifest", str(manifest_path)],
        cwd=ROOT,
        check=True,
    )


