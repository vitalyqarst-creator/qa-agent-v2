def write_text(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content.rstrip() + "\n", encoding="utf-8", newline="\n")


def write_artifact(target: Path, title: str, sections: list[tuple[int, str, str]], manifest_name: str) -> None:
    base = TD / "_sections" / manifest_name
    base.mkdir(parents=True, exist_ok=True)
    section_entries = []
    for idx, (level, heading, content) in enumerate(sections, start=1):
        fname = f"{idx:02d}.md"
        write_text(base / fname, content)
        section_entries.append({"level": level, "heading": heading, "content_file": fname})
    manifest = {"target_path": str(target), "sections": section_entries}
    manifest_path = base / "manifest.json"
    write_text(manifest_path, json.dumps(manifest, ensure_ascii=False, indent=2))
    subprocess.run(
        [sys.executable, str(ROOT / "scripts" / "write_artifact_sections.py"), "--manifest", str(manifest_path)],
        check=True,
        cwd=ROOT,
    )


