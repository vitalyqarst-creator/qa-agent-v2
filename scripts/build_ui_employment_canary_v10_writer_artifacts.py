def table(headers: list[str], rows: list[list[str] | tuple[str, ...]]) -> str:
    out = ["| " + " | ".join(headers) + " |", "| " + " | ".join(["---"] * len(headers)) + " |"]
    for row in rows:
        out.append("| " + " | ".join(str(c) for c in row) + " |")
    return "\n".join(out)


def tc(tc_id: str, title: str, typ: str, priority: str, trace: str, pre: str, data: str, steps: list[str], expected: str, post: str) -> str:
    return "\n".join(
        [
            f"### {tc_id} - {title}",
            "",
            table(
                ["Поле", "Значение"],
                [
                    ("Название", title),
                    ("Тип", typ),
                    ("Приоритет", priority),
                    ("Трассировка", trace),
                ],
            ),
            "",
            "### Предусловия",
            "",
            pre,
            "",
            "### Тестовые данные",
            "",
            data,
            "",
            "### Шаги",
            "",
            "\n".join(f"{i}. {s}" for i, s in enumerate(steps, start=1)),
            "",
            "### Итоговый ожидаемый результат",
            "",
            expected,
            "",
            "### Постусловия",
            "",
            post,
        ]
    )


