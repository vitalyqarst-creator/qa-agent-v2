def source_row_inventory() -> str:
    return md_table(
        ["source_row_id", "package_id", "field_or_action", "source_ref", "requirement_codes", "in_scope", "mapped_atom_or_gap"],
        [[f"`{a}`", f"`{b}`", c, f"`{d}`", e if e else "", f"`{f}`", g] for a, b, c, d, e, f, g in SOURCE_ROWS],
    )


def prop_id(atom_id: str) -> str:
    return atom_id.replace("ATOM", "SP")


def property_type(atom_id: str, statement: str, link: str) -> str:
    if "GAP-" in link:
        return "expected-result"
    if "все и только активные значения" in statement or "чек-бокс" in statement:
        return "table-list"
    if "числов" in statement and "телефон" not in statement:
        return "numeric-format"
    if "10 числовых" in statement:
        return "length"
    if "шаблон" in statement:
        return "default-mask"
    if "подсвеч" in statement:
        return "red-highlight"
    if "открывается раздел" in statement or "открывает раздел" in statement:
        return "action-navigation"
    if "уведомление" in statement:
        return "confirmation-dialog"
    if "отображает блок" in statement or "удалить" in statement:
        return "scenario-use-case"
    if "отображается" in statement or "не отображается" in statement:
        return "visibility"
    if "редакт" in statement or "новое значение" in statement:
        return "editability"
    if "по умолчанию" in statement:
        return "default-value"
    return "field-property"


def source_row_completeness() -> str:
    rows = []
    for src_id, _pkg, _field, _ref, codes, in_scope, mapped in SOURCE_ROWS:
        if in_scope != "yes" or not codes:
            continue
        linked_atoms = "; ".join(a for a, _b, c, _d, _e, _f in ATOMS if src_id in c)
        gaps = "; ".join(sorted({g for g in (mapped or "").replace(",", ";").split(";") if "GAP-" in g}))
        props_list = [prop_id(a) for a, _b, c, _d, _e, _f in ATOMS if src_id in c]
        props_list.extend(extra[1] for extra in EXTRA_NORMALIZATION_GAPS if extra[0] == src_id)
        props = "; ".join(props_list)
        rows.append([f"`{src_id}`", codes, props, linked_atoms, gaps, "covered-or-gap"])
    return md_table(
        ["source_row_id", "source_requirement_codes", "normalized_property_ids", "linked_atoms", "gap_ids", "coverage_decision"],
        rows,
    )


