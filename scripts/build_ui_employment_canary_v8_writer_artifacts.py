def normalization() -> str:
    rows = []
    for a, b, c, d, e, f in ATOMS:
        src = "SRC-020" if a == "ATOM-032" else c.split(";")[0].strip()
        statement = atom(a)[4]
        gap_id = f if "GAP-" in f else ""
        rows.append([
            f"`{src}`",
            f"`{prop_id(a)}`",
            f"`{b}`",
            c.replace(";", ","),
            property_type(a, statement, f),
            "source condition" if "если" in statement.lower() or "при " in statement.lower() else "always/applicable setup",
            statement,
            d,
            source_ref_for(src),
            "unclear" if gap_id else "high",
            gap_id,
            "" if gap_id else a,
        ])
    for src, spid, pkg, field, prop, condition, expected, req, gap in EXTRA_NORMALIZATION_GAPS:
        rows.append([
            f"`{src}`",
            f"`{spid}`",
            f"`{pkg}`",
            field,
            prop,
            condition,
            expected,
            req,
            source_ref_for(src),
            "unclear",
            gap,
            "",
        ])
    return md_table(
        ["source_row_id", "source_property_id", "package_id", "field_or_block", "property", "condition", "expected_behavior", "requirement_code", "source_ref", "confidence", "gap_id", "linked_atoms"],
        rows,
    )


