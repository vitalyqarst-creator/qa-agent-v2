def md_table(headers: list[str], rows: list[list[str] | tuple[str, ...]]) -> str:
    lines = ["| " + " | ".join(headers) + " |", "| " + " | ".join(["---"] * len(headers)) + " |"]
    for row in rows:
        lines.append("| " + " | ".join(str(cell).replace("\n", "<br>").replace("|", "\\|") for cell in row) + " |")
    return "\n".join(lines)


def section_write(target: Path, manifest_name: str, sections: list[tuple[int, str, str]], preamble: str = "") -> None:
    CHUNKS.mkdir(parents=True, exist_ok=True)
    section_entries = []
    preamble_file = ""
    if preamble:
        preamble_file = f"{manifest_name}.00.preamble.md"
        (CHUNKS / preamble_file).write_text(preamble.rstrip() + "\n", encoding="utf-8", newline="\n")
    for index, (level, heading, content) in enumerate(sections, start=1):
        filename = f"{manifest_name}.{index:02d}.section.md"
        (CHUNKS / filename).write_text(content.rstrip() + "\n", encoding="utf-8", newline="\n")
        section_entries.append({"level": level, "heading": heading, "content_file": filename})
    manifest = {"target_path": str(target), "preamble_file": preamble_file, "sections": section_entries}
    manifest_path = CHUNKS / f"{manifest_name}.manifest.json"
    manifest_path.write_text(json.dumps(manifest, ensure_ascii=False, indent=2), encoding="utf-8", newline="\n")
    subprocess.run([sys.executable, str(ROOT / "scripts" / "write_artifact_sections.py"), "--manifest", str(manifest_path), "--dry-run"], check=True)
    subprocess.run([sys.executable, str(ROOT / "scripts" / "write_artifact_sections.py"), "--manifest", str(manifest_path)], check=True)


