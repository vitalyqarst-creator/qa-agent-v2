def first_source_row(source_ref: str) -> str:
    for token in source_ref.replace(";", " ").split():
        if token.startswith("SRC-"):
            return token
    return source_ref.split(";")[0].strip()


def first_requirement_code(req_id: str) -> str:
    for token in req_id.replace(";", " ").split():
        if token.startswith("GSR"):
            # The split above separates GSR and the number; rebuild below.
            continue
    parts = [part.strip() for part in req_id.split(";") if part.strip()]
    for part in parts:
        if part.startswith("GSR "):
            return part
    return parts[0] if parts else "-"


