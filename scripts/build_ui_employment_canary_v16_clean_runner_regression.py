def md_table(headers: list[str], rows: Iterable[Iterable[str]]) -> str:
    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        lines.append("| " + " | ".join(str(cell) for cell in row) + " |")
    return "\n".join(lines)


def write_artifact(target: Path, title: str, body: str, *, level: int = 2) -> None:
    section_root = TD / "artifact-sections" / "v16"
    section_root.mkdir(parents=True, exist_ok=True)
    name = target.stem.replace(".", "-")
    body_path = section_root / f"{name}.body.md"
    manifest_path = section_root / f"{name}.manifest.json"
    body_path.write_text(body.strip() + "\n", encoding="utf-8", newline="\n")
    manifest = {
        "target_path": str(target),
        "sections": [{"level": level, "heading": title, "content_file": str(body_path)}],
    }
    manifest_path.write_text(json.dumps(manifest, ensure_ascii=False, indent=2), encoding="utf-8", newline="\n")
    subprocess.run(["python", str(HELPER), "--manifest", str(manifest_path)], check=True, cwd=ROOT)


def tc_body(
    title: str,
    tc_type: str,
    priority: str,
    package_id: str,
    trace: str,
    preconditions: str,
    data: str,
    steps: list[str],
    expected: str,
    postconditions: str,
) -> str:
    return "\n\n".join(
        [
            f"**Название:** {title}",
            f"**Тип:** {tc_type}",
            f"**Приоритет:** {priority}",
            f"**package_id:** {package_id}",
            f"**Трассировка:** {trace}",
            f"**Предусловия:** {preconditions}",
            f"**Тестовые данные:** {data}",
            "**Шаги:**\n" + "\n".join(f"{i}. {step}" for i, step in enumerate(steps, 1)),
            f"**Итоговый ожидаемый результат:** {expected}",
            f"**Постусловия:** {postconditions}",
        ]
    )


