def build_profile_from_report(report_path: Path | None) -> dict:
    if not report_path or not report_path.exists():
        return {
            "command": "python scripts/validate_agent_artifacts.py --root fts/ft-2-OF_16 --json --output <pending>",
            "generated_by": "codex_review_cycle_runner",
            "scope_slug": SCOPE,
            "canonical_test_cases": f"test-cases/{CANONICAL.name}",
            "test_design_dir": f"work/test-design/{SCOPE}",
            "current_scope_findings": [],
            "unresolved_warning_error_count": 0,
        }
    report = json.loads(report_path.read_text(encoding="utf-8"))
    scope_paths = [
        f"test-cases/{CANONICAL.name}",
        f"work/test-design/{SCOPE}",
        f"work/review-cycles/{SCOPE}",
    ]
    current = []
    for finding in report.get("findings", []):
        path = str(finding.get("path", "")).replace("\\", "/")
        if any(marker in path for marker in scope_paths):
            current.append(
                {
                    "id": finding.get("id", ""),
                    "severity": finding.get("severity", ""),
                    "path": path,
                    "status": "open",
                    "title": finding.get("title", ""),
                    "recommended_action": finding.get("recommended_action", ""),
                    "evidence": finding.get("evidence", []),
                }
            )
    unresolved = sum(1 for item in current if item["severity"] in {"warning", "error"} and item["status"] == "open")
    return {
        "command": f"python scripts/validate_agent_artifacts.py --root fts/ft-2-OF_16 --json --output {report_path.as_posix()}",
        "generated_by": "codex_review_cycle_runner",
        "scope_slug": SCOPE,
        "canonical_test_cases": f"test-cases/{CANONICAL.name}",
        "test_design_dir": f"work/test-design/{SCOPE}",
        "current_scope_findings": current,
        "unresolved_warning_error_count": unresolved,
    }


